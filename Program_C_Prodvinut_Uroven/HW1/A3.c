/*-------------------------------------------------------------------
 * A3.c
 * ------------------------------------------------------------------
 * Извлечение бит
 * ------------------------------------------------------------------
 * На вход программе подается беззнаковое 32-битное целое число N и 
 * натуральное число K (1 ≤ K ≤ 31). Требуется взять K младших битов 
 * числа N и вывести полученное таким образом число.
 * ------------------------------------------------------------------
 * Формат входных данных
 * ------------------------------------------------------------------	
 * Формат результата
 * ------------------------------------------------------------------
 */

#include <stdio.h>
#include <inttypes.h>

int main(void)
{
    uint32_t N;  // беззнаковое 32-битное число
    uint32_t K;  // количество младших битов, которые нужно оставить (1..31)

    // Считываем N
    if (scanf("%" SCNu32, &N) != 1) 
    {
        return 1;   // ошибка ввода
    }

    // Считываем K
    if (scanf("%" SCNu32, &K) != 1) 
    {
        return 1;   // ошибка ввода
    }

    // По условию 1 ≤ K ≤ 31, но на всякий случай можно проверить
    if (K == 0 || K > 31) 
    {
        return 1;   // некорректный K
    }

    // Формируем маску из K единичных битов:
    //   K = 3  -> (1u << 3) = 0b1000, минус 1 = 0b0111
    //   K = 5  -> (1u << 5) = 0b100000, минус 1 = 0b011111
    uint32_t mask = (1u << K) - 1u;

    // Оставляем только K младших битов числа N:
    uint32_t result = N & mask;

    // Выводим результат
    printf("%" PRIu32 "\n", result);

    return 0;
}