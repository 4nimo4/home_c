    head->tail = tail;                                               // –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ö–≤–æ—Å—Ç –∑–º–µ–π–∫–µ
    head->tsize = size;                                              // –∑–∞–¥–∞—ë–º —Ç–µ–∫—É—â—É—é –¥–ª–∏–Ω—É —Ö–≤–æ—Å—Ç–∞ (START_TAIL_SIZE)
    //head->controls = default_controls;                               // –Ω–∞–∑–Ω–∞—á–∞–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–µ –∫–ª–∞–≤–∏—à–∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
