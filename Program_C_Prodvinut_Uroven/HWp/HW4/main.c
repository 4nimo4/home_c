//------------------------------------------------------

// –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç –ª–∏ –ø–æ–∑–∏—Ü–∏—è –≥–æ–ª–æ–≤—ã —Å –∫–∞–∫–∏–º-–ª–∏–±–æ —Å–µ–≥–º–µ–Ω—Ç–æ–º —Ö–≤–æ—Å—Ç–∞.
// –ï—Å–ª–∏ –¥–∞ ‚Äî –∑–Ω–∞—á–∏—Ç, –∑–º–µ–π–∫–∞ –≤—Ä–µ–∑–∞–ª–∞—Å—å —Å–∞–º–∞ –≤ —Å–µ–±—è.
int isSelfCollision(const snake_t *s)
{
    for (size_t i = 0; i < s->tsize; ++i)           // –ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º —Å–µ–≥–º–µ–Ω—Ç–∞–º —Ö–≤–æ—Å—Ç–∞
    {
        if (s->tail[i].x == s->x &&                // —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º x –≥–æ–ª–æ–≤—ã –∏ x —Å–µ–≥–º–µ–Ω—Ç–∞
            s->tail[i].y == s->y)                  // –∏ y –≥–æ–ª–æ–≤—ã –∏ y —Å–µ–≥–º–µ–Ω—Ç–∞
            return 1;                              // –µ—Å–ª–∏ —Å–æ–≤–ø–∞–ª–æ ‚Äî —Å–æ–æ–±—â–∞–µ–º –æ —Å—Ç–æ–ª–∫–Ω–æ–≤–µ–Ω–∏–∏
    }
    return 0;                                      // —Å—Ç–æ–ª–∫–Ω–æ–≤–µ–Ω–∏—è –Ω–µ—Ç
}

