// –†–∞–∑–º–µ—Å—Ç–∏—Ç—å –µ–¥—É –Ω–∞ –ø–æ–ª–µ
void putFood(food_t f[], size_t number_seeds)//–ø–µ—Ä–µ–¥–∞—ë–º –º–∞—Å—Å–∏–≤ –µ–¥—ã, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∑–µ—Ä–µ–Ω
{
    for(size_t i=0; i<number_seeds; i++)
    {
        putFoodSeed(f+i);//–∑–∞—Å–µ–≤–∞–µ–º –Ω–∞—à–µ –ø–æ–ª–µ –∑–µ—Ä–Ω–∞–º–∏
    }
}
/*
–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –µ–¥—ã. –ï—Å–ª–∏ —á–µ—Ä–µ–∑ –∫–∞–∫–æ–µ-—Ç–æ –≤—Ä–µ–º—è(FOOD_EXPIRE_SECONDS) —Ç–æ—á–∫–∞
—É—Å—Ç–∞—Ä–µ–≤–∞–µ—Ç, –∏–ª–∏ –∂–µ –æ–Ω–∞ –±—ã–ª–∞ —Å—ä–µ–¥–µ–Ω–∞(food[i].enable==0ÓÇÇ, —Ç–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –µ—ë
–ø–æ–≤—Ç–æ—Ä–Ω–∞—è –æ—Ç—Ä–∏—Å–æ–≤–∫–∞ –∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏ ‚Äî refreshFood(food, SEED_NUMBER)
*/

void refreshFood(food_t f[], int nfood)
{
    char spoint[2] = {0};

    int max_x, max_y;
    razmer_okna(&max_y, &max_x);  // –ø–æ–ª—É—á–∞–µ–º ¬´–ø–æ—Å–ª–µ–¥–Ω–∏–µ –∏–Ω–¥–µ–∫—Å—ã¬ª –ø–æ X –∏ Y
   
    for(int i=0; i<nfood; i++)
    {
        if( f[i].put_time )
        {
            if( !f[i].enable || (time(NULL) - f[i].put_time) > FOOD_EXPIRE_SECONDS )
            {
                putFoodSeed(f+i);
            }   
        }
    }
}

/*
–°—ä–µ—Å—Ç—å –∑–µ—Ä–Ω–æ
–¢–∞–∫–æ–µ —Å–æ–±—ã—Ç–∏–µ –≤–æ–∑–Ω–∏–∫–∞–µ—Ç, –∫–æ–≥–¥–∞ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –≥–æ–ª–æ–≤—ã —Å–æ–≤–ø–∞–¥–∞—é—Ç —Å –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–æ–π
–∑–µ—Ä–Ω–∞. –í —ç—Ç–æ–º —Å–ª—É—á–∞–µ –∑–µ—Ä–Ω–æ –ø–æ–º–µ—á–∞–µ—Ç—Å—è –∫–∞–∫ enable=0.
–ü—Ä–æ–≤–µ—Ä–∫–∞ —Ç–æ–≥–æ, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –∫–∞–∫–æ–µ-—Ç–æ –∏–∑ –∑–µ—Ä–µ–Ω —Å—ä–µ–¥–µ–Ω–Ω—ã–º, –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –ø—Ä–∏
–ø–æ–º–æ—â–∏ —Ñ—É–Ω–∫—Ü–∏–∏ –ª–æ–≥–∏—á–µ—Å–∫–æ–π —Ñ—É–Ω–∫—Ü–∏–∏ haveEat –≤ —ç—Ç–æ–º —Å–ª—É—á–∞–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç
—É–≤–µ–ª–∏—á–µ–Ω–∏–µ —Ö–≤–æ—Å—Ç–∞ –Ω–∞ 1 —ç–ª–µ–º–µ–Ω—Ç.
*/
_Bool haveEat(snake_t *head, food_t f[], int nfood)
{
    for(size_t i=0; i<nfood; i++)
        if( f[i].enable && head->x == f[i].x && head->y == f[i].y)
        {
            f[i].enable = 0;
            return 1;
        }
    return 0;
}
