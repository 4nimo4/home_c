#define EPS_ROOT 1e-6        //(10^-6)Константа: требуемая точность по x при поиске корней (eps1 из задания)
#define EPS_INT  1e-6        //(10^-6)Константа: требуемая точность по значению интеграла (eps2 из задания)
#define MAX_ITER 1000000     //Максимальное число итераций в итерационных методах (root), защита от зацикливания
#define DERIV_EPS 1e-12      //Чтобы избежать деления на почти ноль в формуле Ньютона 
                             //e-12 выбрано как «достаточно маленькое» число для double по сравнению 
                             //с нашими масштабами и точностью 1e-6.
#define TEST_ROOT_A 0.0      //проверочные значения для тестовых функций 
#define TEST_ROOT_B 2.0      //проверочные значения для тестовых функций 
#define TEST_INTEGR_A 0.0    //проверочные значения для тестовых функций 
#define TEST_INTEGR_B 1.0    //проверочные значения для тестовых функций 

// Определяем тип function как "указатель на функцию, принимающую double и возвращающую double"
typedef double (*function)(double);

//------------------------------------------------------ 
//          Исходные функции
//------------------------------------------------------
// Определение функции f1(x) = 0.6x + 3
double f1(double x)         // Объявляем функцию f1, принимающую double x
{    
    return 0.6 * x + 3.0;    
}

// Определение функции f2(x) = (x − 2)^3 – 1
double f2(double x)         // Объявляем функцию f2, принимающую double x
{    
    double t = x - 2.0;      // Вводим вспомогательную переменную t = (x - 2)
    return t * t * t - 1.0;  // Возвращаем t^3 - 1, то есть (x-2)^3 - 1
}

// Определение функции f3(x) = 3 / x
double f3(double x)         // Объявляем функцию f3, принимающую double x
{
    return 3.0 / x;          // Возвращаем значение 3/x
}

//------------------------------------------------------ 
// Вычисляем производные для использования в методе Ньютона
//------------------------------------------------------
// Первая производная f1'(x) = 0.6 (константа, не зависит от x)
double df1(double x)        // Объявляем функцию df1, формально принимающую double x
{
    (void)x;                 // Явно помечаем x как неиспользуемый, чтобы не было предупреждения от компилятора
    return 0.6;              // Возвращаем постоянное значение 0.6
}

// Первая производная f2'(x) для f2(x) = (x-2)^3 - 1 -> f2'(x) = 3(x-2)^2
double df2(double x)        // Объявляем функцию df2, принимающую double x
{    
    double t = x - 2.0;      // t = (x - 2)
    return 3.0 * t * t;      // Возвращаем 3*(x-2)^2
}

// Первая производная f3'(x) для f3(x) = 3/x -> f3'(x) = -3/x^2
double df3(double x)        // Объявляем функцию df3, принимающую double x
{
    return -3.0 / (x * x);   // Возвращаем -3 / (x^2)
}

//------------------------------------------------------ 
// Вспомогательные разности для удобства вычисления методом Ньютона
//------------------------------------------------------
// Возвращает значение разности (f - g)(x) = f(x) - g(x)
double diff_fg(function f, function g, double x)  // Принимает два указателя на функции f, g и аргумент x
{    
    return f(x) - g(x);                            // Вычисляет f(x) и g(x), возвращает их разность
}

// Возвращает значение разности производных (f' - g')(x) = df(x) - dg(x)
double ddiff_fg(function f, function g,            // Указатели на исходные функции (формально не используем)
                function df, function dg,          // Указатели на их производные df, dg
                double x)                          // Аргумент x
{
    (void)f;                                       // Помечаем f как неиспользуемую переменную
    (void)g;                                       // Помечаем g как неиспользуемую переменную
    return df(x) - dg(x);                          // Возвращаем df(x) - dg(x)
}

//------------------------------------------------------ 
// Функция root() (метод Ньютона)
//------------------------------------------------------
// Универсальная функция поиска корня уравнения f(x) = g(x) на отрезке [a, b]
// Используется метод Ньютона с производными df и dg.
// eps — требуемая точность по x (EPS_ROOT),
// iters — указатель на переменную, куда запишется количество сделанных итераций.
double root(function f, function g,         // Указатели на функции f(x) и g(x)
            function df, function dg,       // Указатели на их производные f'(x) и g'(x)
            double a, double b,             // Левая и правая границы отрезка [a, b]
            double eps,                     // Требуемая точность по x (условие остановки)
            int *iters)                     // Указатель на счётчик итераций
{
    double x = 0.5 * (a + b); // Начальное приближение x0 — середина отрезка [a, b]
    int k = 0;                // Счётчик итераций, изначально 0

    while (k < MAX_ITER)     // Основной цикл метода Ньютона, ограничен MAX_ITER, чтобы избежать зацикливания
    {    
        double fx  = diff_fg(f, g, x);      // Вычисляем значение разности F(x) = f(x) - g(x)
        double dfx = ddiff_fg(f, g, df, dg, x); // Вычисляем производную F'(x) = f'(x) - g'(x)

        if (fabs(dfx) < DERIV_EPS)             // Если |F'(x)| слишком мало (почти ноль), -
            //производная, которая меньше 10^-12 , уже настолько мала, что шаг fx/dfx
            //может стать безумно большим относительно наших масштабов.
        {    
            // то шаг Ньютона x - F(x)/F'(x) становится нестабильным,
            // поэтому останавливаемся (выходим из цикла)
            break;
        }

        double x_next = x - fx / dfx;       // Формула метода Ньютона: x_{n+1} = x_n - F(x_n)/F'(x_n)
        k++;                                // Считаем ещё одну итерацию

        if (fabs(x_next - x) < eps)        // Если модуль приращения по x меньше eps — достигнута нужная точность
        {    
            x = x_next;                     // Сохраняем новое значение x
            break;                          // Выходим из цикла
        }

        x = x_next;                         // Иначе просто переходим к следующей итерации с обновлённым x
    }

    if (iters)                             // Если указатель iters не NULL
    {    
        *iters = k;                         // Записываем туда количество итераций k
    }

    return x;                               // Возвращаем найденное приближение корня
}

//------------------------------------------------------ 
//  Функция integral() (Симпсон)
//------------------------------------------------------
// Численное вычисление определённого интеграла ∫[a,b] f(x) dx
// Метод Симпсона с удвоением числа разбиений до достижения точности eps.
double integral(function f, double a, double b, double eps)
{
    int n = 2;           // Начинаем с 2 равных отрезков (минимальное чётное число разбиений)
    double prev = 0.0;   // Предыдущее значение интеграла для критерия сходимости

    while (1)           // Бесконечный цикл, из которого выйдем через return
    {    
        double h = (b - a) / n;   // Шаг сетки: расстояние между соседними точками разбиения
        double sum1 = 0.0;        // Сумма значений функции в "нечётных" узлах (индексы 1,3,5,...)
        double sum2 = 0.0;        // Сумма значений функции в "чётных" узлах (индексы 2,4,6,...)

        for (int i = 1; i < n; i++)  // Проходим по всем внутренним узлам разбиения
        {    
            double x = a + i * h;     // Текущая точка x_i = a + i*h
            if (i % 2 == 0)           // Если i чётное
                sum2 += f(x);         // добавляем f(x) в сумму чётных узлов
            else                      // Если i нечётное
                sum1 += f(x);         // добавляем f(x) в сумму нечётных узлов
        }

        // Формула Симпсона:
        // I ≈ h/3 * [ f(a) + f(b) + 4*sum1 + 2*sum2 ]
        double I = (h / 3.0) * (f(a) + f(b) + 4.0 * sum1 + 2.0 * sum2);

        // Проверяем сходимость, начиная с момента, когда уже есть минимум два разных значения (n > 2)
        if (n > 2 && fabs(I - prev) < eps)  // Если разность текущего и предыдущего значений меньше eps
        {    
            return I;                         // Считаем, что интеграл посчитан с нужной точностью и возвращаем I
        }

        prev = I;     // Сохраняем текущее значение интеграла как "предыдущее" для следующего шага
        n *= 2;       // Удваиваем число разбиений n (делаем сетку более частой)

        if (n > 1e7)  // Если число разбиений стало слишком большим (защита от бесконечного цикла)
        {
            return I;   // Возвращаем текущее значение I (как есть)
        }
    }
}

