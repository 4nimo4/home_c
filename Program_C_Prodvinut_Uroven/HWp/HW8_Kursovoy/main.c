//---------------------------------------------------- 
//Вспомогательная функция: определяет, какая из двух функций выше на середине отрезка [a, b]
//------------------------------------------------------
// Возвращает +1, если f(mid) > g(mid), иначе -1.
int is_above(function f, function g, double a, double b) 
{
    double mid = 0.5 * (a + b);          // Находим середину отрезка [a, b]
    return (f(mid) > g(mid)) ? 1 : -1;   // Если f(mid) > g(mid), возвращаем 1, иначе -1
}

