#include <ncurses.h>   // Библиотека ncurses для работы с текстовым интерфейсом в терминале
#include <unistd.h>    // Для функции sleep() (здесь не используется, но может пригодиться)

int main() 
{
    // Инициализируем режим ncurses.
    // Захватывает экран терминала и готовит его для «рисования» через ncurses.
    initscr();

    // Строка, которую будем выводить на экран
    char string[] = "Hello World!";
    // Координаты для позиционного вывода (x — столбец, y — строка)
    int x = 20, y = 5;

    // Вывести строку на стандартный экран stdscr в ТЕКУЩЕЙ позиции курсора.
    // В начале работы ncurses курсор стоит в левом верхнем углу (0,0).
    printw("%s", string);

    // Вывести ту же строку в позицию (y, x).
    // mvprintw(y, x, ...) сначала перемещает курсор, затем печатает строку.
    mvprintw(y, x, "%s", string);

    // При необходимости можно явно обновить экран:
    // refresh();
    // (часто ncurses обновляет экран при ожидании ввода, но для сложных программ
    // refresh() вызывают явно после серийных выводов.)

    // Ожидаем нажатия любой клавиши, чтобы окно не закрылось сразу
    getch();

    // Завершаем работу ncurses и возвращаем терминал в нормальный режим
    endwin();

    return 0;   // Успешное завершение программы
}