// –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ __int128 –≤ —Ç–∞–±–ª–∏—Ü–µ –ø–æ–∑–≤–æ–ª—è–µ—Ç –±–µ–∑–æ–ø–∞—Å–Ω–µ–µ —Ö—Ä–∞–Ω–∏—Ç—å –±–æ–ª—å—à–∏–µ —Ñ–∞–∫—Ç–æ—Ä–∏–∞–ª—ã,
// –Ω–æ –¥–∞–∂–µ __int128 –ø–µ—Ä–µ–ø–æ–ª–Ω–∏—Ç—Å—è –¥–ª—è –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –±–æ–ª—å—à–∏—Ö n (~34! —É–∂–µ > 2^127).
