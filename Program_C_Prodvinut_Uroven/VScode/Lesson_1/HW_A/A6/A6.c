/*-------------------------------------------------------------------
 * A6.c
 * ------------------------------------------------------------------
 * Инверсия старших
 * ------------------------------------------------------------------
 * На вход программе подается беззнаковое 32-битное целое число N. 
 * Требуется изменить значения всех битов старшего байта числа на 
 * противоположные и вывести полученное таким образом число.
 * ------------------------------------------------------------------
 * Формат входных данных
 * ------------------------------------------------------------------	
 * Формат результата
 * ------------------------------------------------------------------
 */

#include <stdio.h>
#include <inttypes.h>

int main(void)
{
    uint32_t N;  // беззнаковое 32-битное число

    // Считываем N
    if (scanf("%" SCNu32, &N) != 1) {
        return 1; // ошибка ввода
    }

    // Маска для старшего байта:
    // 0xFF000000 в двоичном виде: 11111111 00000000 00000000 00000000
    // Оператор ^ (XOR) инвертирует биты там, где в маске 1.
    uint32_t result = N ^ 0xFF000000u;

    // Выводим результат
    printf("%" PRIu32 "\n", result);

    return 0;
}