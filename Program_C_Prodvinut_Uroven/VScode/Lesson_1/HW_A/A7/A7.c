/*-------------------------------------------------------------------
 * A7.c
 * ------------------------------------------------------------------
 * Не пара
 * ------------------------------------------------------------------
 * Преподаватели Geek Brains создали набор, содержащий N 
 * (1 ≤ N ≤ 1 000 000) натуральных чисел, не превосходящих 1 000 000. 
 * Известно, что ровно одно число в этом наборе встречается один раз, 
 * а остальные — ровно по два раза. Помогите им найти это число. 
 * Входные данные: в первой входной строке вводится число N, затем 
 * по одному числу в строке вводятся N натуральных чисел, 
 * не превосходящих 2000 000 000. Выходные данные: ваша программа 
 * должна определить число, встречающееся один раз, и вывести его 
 * на экран. Указание: использовать массивы запрещается.
 * ------------------------------------------------------------------
 */

#include <stdio.h>

int main(void)
{
    int N;          // количество чисел во входе
    long long x;    // очередное число (по условию до 2 000 000 000 — влезает в signed 32, но возьмём long long с запасом)
    long long result = 0;  // накопленный XOR всех чисел

    // Считываем N
    if (scanf("%d", &N) != 1 || N < 1) {
        return 0; // некорректный ввод
    }

    // Читаем N чисел и по очереди XOR-им их
    for (int i = 0; i < N; i++) {
        if (scanf("%lld", &x) != 1) {
            return 0; // ошибка ввода
        }
        result ^= x; // XOR с накопленным результатом
    }

    // В result осталось число, которое встретилось один раз
    printf("%lld\n", result);

    return 0;
}