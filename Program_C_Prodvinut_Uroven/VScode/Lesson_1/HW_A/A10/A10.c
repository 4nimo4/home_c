/*-------------------------------------------------------------------
 * A10.c
 * ------------------------------------------------------------------
 * Распаковать порядок
 * ------------------------------------------------------------------
 * Необходимо реализовать программу, которой на входе передаётся 
 * вещественное число в типе float, она возвращает порядок который 
 * хранится в поле EXP в виде десятичного целого числа. 
 * Функция должна строго соответствовать прототипу
 * int extractExp(float)
 * ------------------------------------------------------------------
 * Нужно вытащить экспоненту из представления числа float
по стандарту IEEE‑754 (одинарная точность):

1 бит — знак
8 бит — экспонента (поле EXP)
23 бита — мантисса

У float экспонента хранится как смещённая:
E_хранимое = E_истинное + 127.
В задаче, судя по формулировке, нужно вернуть именно то, 
что хранится в поле EXP (т.е. «сырое» значение 0..255), 
а не истинный показатель степени.

Сделаем это через
union , чтобы интерпретировать те же биты как
uint32_t:
 */

#include <stdio.h>
#include <stdint.h>

// Извлекает поле EXP (8 бит) из представления числа float.
// Возвращает СЫРОЕ значение экспоненты (0..255), как оно хранится в памяти.
int extractExp(float x)
{
    // Объединение, позволяющее посмотреть на те же биты числа x как на 32-битовое целое
    union {
        float f;
        uint32_t u;
    } v;

    v.f = x;  // записываем float

    // Формат float (IEEE 754):
    // [ знак (1 бит) | EXP (8 бит) | мантисса (23 бита) ]
    //
    // Чтобы получить EXP:
    // 1) сдвигаем вправо на 23 бита (убираем мантиссу)
    // 2) берём младшие 8 бит (& 0xFF)
    int exp = (int)((v.u >> 23) & 0xFFu);

    return exp;
}

int main(void)
{
    float x;
    if (scanf("%f", &x) != 1) {
        return 1;
    }

    int e = extractExp(x);
    printf("%d\n", e);

    return 0;
}