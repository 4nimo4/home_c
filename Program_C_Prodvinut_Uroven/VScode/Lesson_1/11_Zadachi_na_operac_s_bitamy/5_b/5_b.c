//--------------------------------------------------------
//   Напишите функцию, которая возвращает истину, если знаки двух
//   заданных 32‑битных чисел разные. Использовать операции сравнения
//   запрещено
//--------------------------------------------------------

#include <stdio.h>
#include <inttypes.h>

// Функция возвращает 1 (true), если знаки чисел a и b разные,
// и 0 (false), если знаки одинаковые.
_Bool difSign(int32_t a, int32_t b) 
{
    // Идея:
    // В 32‑битном знаковом int старший бит (бит 31) — бит знака:
    //   0 -> число >= 0
    //   1 -> число < 0
    //
    // При арифметическом сдвиге вправо на 31 бит:
    //   a >> 31 -> 0, если a >= 0
    //              -1 (0xFFFFFFFF), если a < 0
    //   b >> 31 -> 0 или -1 аналогично.
    //
    // Далее XOR:
    //   0  ^ 0  = 0  (оба неотрицательные)
    //   -1 ^ -1 = 0  (оба отрицательные, все биты одинаковы)
    //   0  ^ -1 = -1 (знаки разные)
    //
    // В контексте _Bool любое ненулевое значение считается true.
    // Значит:
    //   результат != 0 → разные знаки → true
    //   результат == 0 → одинаковые знаки → false
    return (a >> 31) ^ (b >> 31);
}

int main(void)
{
    //int32_t a = 0xFF078C3F, b = 0x56fA63BD;
    int32_t a = 3, b = 5; // оба положительные → знаки одинаковые

    printf("a = %x  b = %x\n", a, b); 

    // Если difSign(a, b) вернёт true (разные знаки) → "YES",
    // иначе → "NO".
    difSign(a, b) ? printf("YES\n") : printf("NO\n");

    return 0;
}