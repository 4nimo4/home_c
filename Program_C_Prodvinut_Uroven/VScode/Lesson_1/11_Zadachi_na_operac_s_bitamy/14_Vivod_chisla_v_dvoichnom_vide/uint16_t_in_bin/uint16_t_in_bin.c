#include <stdio.h>
#include <stdint.h>

// --------------------------------------------------------
//  Вывод 16‑битного БЕЗЗНАКОВОГО числа (uint16_t) в двоичном виде
// --------------------------------------------------------
// Функция печатает РОВНО 16 бит числа x: от старшего (бит 15) к младшему (бит 0).
// Для наглядности выводится пробел после каждых 4 бит, пример:
//   0000 0000 0110 1100
void print_binary_uint16(uint16_t x)
{
    // Идём по битам от 15 до 0 (для 16‑битного числа)
    for (int i = 15; i >= 0; --i) {

        // (1u << i) — маска с единицей в позиции i:
        //   i = 15 → 1000 0000 0000 0000₂
        //   i = 14 → 0100 0000 0000 0000₂
        //   ...
        //   i =  0 → 0000 0000 0000 0001₂
        //
        // x & (1u << i) проверяет, установлен ли i‑й бит:
        //   результат != 0 → бит = 1
        //   результат == 0 → бит = 0
        putchar( (x & (1u << i)) ? '1' : '0' );

        // Для удобства чтения отделяем группы по 4 бита:
        // 0000 0000 0110 1100
        if (i % 4 == 0)
            putchar(' ');
    }
}

int main(void)
{
    uint16_t a = 0x06CC; // пример: 0x06CC = 1740₁₀

    // Выводим число в десятичном и шестнадцатеричном виде
    printf("a = %u (0x%04x)\n", a, a);

    // Выводим число в двоичном виде
    printf("a (bin) = ");
    print_binary_uint16(a);
    putchar('\n');

    return 0;
}