//есть ли такие спецификаторы для функции printf("%XXXXX\n", res);  
//--------------------------------------------------------
/*
В стандартном C нет спецификатора формата для двоичного вывода (типа %b).
printf «из коробки» умеет только: %d/%i— десятичное,%x/%X— шестнадцатеричное,
%o— восьмеричное, и т.д., но не двоичное.

Чтобы вывести число в двоичном виде, обычно делают свою функцию.

*/
// --------------------------------------------------------
//   Пример: вывод числа в двоичном виде с помощью собственной функции
//   (для типа uint32_t — 32-битное беззнаковое целое)
// --------------------------------------------------------

#include <stdio.h>
#include <stdint.h>

// Функция выводит 32-битное беззнаковое число x в двоичном виде.
// Формат: 32 бита, начиная со старшего, с пробелом каждые 4 бита.
void print_binary_uint32(uint32_t x)
{
    // Идём по битам от 31 до 0 (для 32-битного числа).
    // i — номер бита: 31 — самый старший, 0 — младший.
    for (int i = 31; i >= 0; --i) {

        // (1u << i) — маска с 1 в позиции i и нулями в остальных.
        // x & (1u << i) — проверка, установлен ли i‑й бит в x.
        //
        // Если результат ненулевой → бит равен 1 → печатаем '1',
        // иначе → бит равен 0 → печатаем '0'.
        putchar( (x & (1u << i)) ? '1' : '0' );

        // Для удобства чтения выводим пробел после каждых 4 бит.
        // Пример: 0000 0000 0000 0000 0000 0000 0011 1111
        if (i % 4 == 0)
            putchar(' ');
    }
}

int main(void)
{
    // Пример числа: 0x3F = 63 (в двоичном: 0000...0011 1111)
    uint32_t a = 63;

    // Выводим число в десятичном виде
    printf("a = %u\n", a);

    // Выводим число в двоичном виде
    printf("a (bin) = ");
    print_binary_uint32(a);
    putchar('\n'); // перевод строки после двоичного вывода

    return 0;
}