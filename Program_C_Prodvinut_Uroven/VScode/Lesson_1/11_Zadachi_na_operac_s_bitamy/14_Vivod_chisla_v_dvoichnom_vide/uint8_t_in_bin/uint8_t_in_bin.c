#include <stdio.h>
#include <stdint.h>

// --------------------------------------------------------
//  Вывод 8‑битного БЕЗЗНАКОВОГО числа (uint8_t) в двоичном виде
// --------------------------------------------------------
// Функция печатает РОВНО 8 бит числа x: от старшего (бит 7) к младшему (бит 0).
// Формат для наглядности: 4 бита + пробел + 4 бита, например:
//   0011 1111
void print_binary_uint8(uint8_t x)
{
    // Идём по битам от 7 до 0 (для 8‑битного числа)
    for (int i = 7; i >= 0; --i) {

        // (1u << i) — маска с единицей в позиции i
        //   i = 7 → 1000 0000₂
        //   i = 6 → 0100 0000₂
        //   ...
        //   i = 0 → 0000 0001₂
        //
        // x & (1u << i) проверяет, установлен ли бит i:
        //   != 0 → бит = 1
        //   == 0 → бит = 0
        putchar( (x & (1u << i)) ? '1' : '0' );

        // После вывода битов 7..4 (то есть когда i == 4)
        // вставляем пробел для удобства чтения:
        //   0101 1110
        if (i == 4)
            putchar(' ');
    }
}

int main(void)
{
    uint8_t a = 0x3F; // 0x3F = 63₁₀ = 0011 1111₂

    // Выводим число в десятичном виде
    printf("a = %u\n", a);

    // Выводим число в двоичном виде
    printf("a (bin) = ");
    print_binary_uint8(a);
    putchar('\n'); // перевод строки после двоичного вывода

    return 0;
}