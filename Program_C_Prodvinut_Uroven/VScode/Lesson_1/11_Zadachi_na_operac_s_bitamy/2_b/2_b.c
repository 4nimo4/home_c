//--------------------------------------------------------
//   Проверка равенства двух целых чисел без арифметики
//   и без операторов сравнения (==, !=, <, > ...)
//--------------------------------------------------------

#include <stdio.h>

// Функция возвращает 1 (true), если a и b равны, и 0 (false) иначе.
_Bool isEqual(int a, int b) 
{
    // Оператор ^ — побитовое XOR.
    // Свойство:
    //   a ^ b == 0  тогда и только тогда, когда a и b равны.
    //
    // Примеры:
    //   5 ^ 5 = 0
    //   5 ^ 3 ≠ 0
    //
    // Выражение (a ^ b) даёт 0, если числа равны.
    // Оператор ! (логическое НЕ) преобразует:
    //   !0  → 1  (true)
    //   !не_ноль → 0 (false)
    //
    // Поэтому:
    //   !(a ^ b) → 1, если a == b
    //              0, если a != b
    return !(a ^ b);
}

int main()
{
    int a = 4, b = 4;

    // Тернарный оператор:
    //   isEqual(a, b) ? printf("YES\n") : printf("NO\n");
    //
    // Если isEqual(a, b) вернуло 1 (true) → печатаем "YES",
    // иначе → "NO".
    isEqual(a, b) ? printf("YES\n") : printf("NO\n");

    return 0;
}