#include <stdio.h>
#include <inttypes.h>

// Функция: если число a чётное — вычесть 1, если нечётное — оставить как есть
int div1IfEven(int a) 
{
    // Побитовое И с 1:
    //  a & 0x1 даёт:
    //    0 для чётных чисел (последний бит 0),
    //    1 для нечётных (последний бит 1).
    //
    // Логическое НЕ:
    //  !(a & 0x1) даёт:
    //    1, если a чётное  (0 -> 1),
    //    0, если a нечётное (1 -> 0).
    //
    // Поэтому:
    //   a - !(a & 0x1)
    //   = a - 1  для чётных чисел
    //   = a - 0  для нечётных
    return a - !(a & 0x1);//вычитаем 1 из четных чисел
    //return a - (a & 0x1);//вычитаем 1 из нечетных чисел
}

int main(void)
{
    int a, res;

    // Считываем целое число из ввода
    scanf("%d", &a);

    // Вычисляем результат по правилу выше
    res = div1IfEven(a);

    // Печатаем результат
    printf("%d\n", res);

    return 0;
}