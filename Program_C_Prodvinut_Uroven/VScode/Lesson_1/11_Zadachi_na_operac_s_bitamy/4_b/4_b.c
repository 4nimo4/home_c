//--------------------------------------------------------
//   Инвертируйте 5 младших битов переменной x типа uint32_t,
//   остальные биты оставьте без изменений
//--------------------------------------------------------

#include <stdio.h>
#include <inttypes.h>

// Функция инвертирует (меняет 0 на 1 и 1 на 0) 5 младших битов числа.
// Остальные биты остаются такими, как были.
int32_t inverse5bits(int32_t n)
{
    // Маска 0x1F в двоичном виде:
    //   0x1F = 0001 1111₂
    //
    // Оператор ^ — побитовое XOR:
    //   бит ^ 1 → инвертирует бит
    //   бит ^ 0 → оставляет бит без изменений
    //
    // Поэтому:
    //   n ^ 0x1F
    // инвертирует только 5 младших битов (b0..b4), а старшие биты (b5..b31)
    // остаются такими же.
    return n ^ 0x1F;
}

int main(void)
{
    int32_t a = 0xFF; // 0xFF = 1111 1111₂

    printf("%x\n", a); // печатаем исходное значение (ff)

    a = inverse5bits(a);
    // Было: a = 1111 1111₂
    // Маска:    0001 1111₂
    // Результат XOR:
    //          1110 0000₂ = 0xE0
    printf("%x\n", a); // e0

    a = inverse5bits(a);
    // Повторное применение восстановит исходные 5 младших битов:
    // 0xE0 ^ 0x1F = 0xFF
    printf("%x\n", a); // ff

    return 0;
}
