//--------------------------------------------------------
//   Напишите функцию, которая сбрасывает установленный крайний
//   правый (младший) 1‑бит целого числа
//--------------------------------------------------------

#include <stdio.h>
#include <inttypes.h>

// Функция возвращает число n, в котором сброшен (обнулён)
// самый правый установленный бит (младший 1‑бит).
//
// Если n = 0, то результата тоже 0 (сбрасывать нечего).
int32_t unset_rightmost (uint32_t n) 
{
    // Классический трюк:
    //   n & (n - 1)
    //
    // Свойство:
    //  - (n - 1) инвертирует все биты справа от младшей единицы
    //    и саму эту единицу превращает в 0.
    //  - Побитовое И с исходным n "съедает" эту младшую единицу,
    //    оставляя остальные биты без изменений.
    //
    // Пример:
    //   n        = 1100 0000 0000 0000
    //   n - 1    = 1011 1111 1111 1111
    //   n&(n-1)  = 1000 0000 0000 0000  (младшая 1 исчезла)
    return n & (n - 1);
}

/*
    Пример побитовой работы:

        n      = 1100 0000 0000 0000
      n - 1    = 1011 1111 1111 1111
    --------------------------------
      n&(n-1)  = 1000 0000 0000 0000
*/

int main(void)
{
    int32_t a, res;

    // Считываем целое число из ввода (как знаковое)
    scanf("%d", &a);

    // Сбрасываем крайний правый установленный бит
    res = unset_rightmost(a);

    // Печатаем результат в десятичном виде
    printf("%d\n", res);

    return 0;
}