//--------------------------------------------------------
//  Напишите логическую функцию. Определите, является ли введённое
//  целое число степенью 4
//--------------------------------------------------------

#include <stdio.h>
#include <inttypes.h>

// Функция возвращает 1 (true), если n — степень 4 (1, 4, 16, 64, ...),
// и 0 (false) в противном случае.
_Bool isPowerOfFour(uint32_t n)
{
    // Условие разбито на три части:
    //
    // 1) n != 0
    //    Степени 4: 1, 4, 16, ... — никогда не 0.
    //
    // 2) (n & (n - 1)) == 0
    //    Проверка "степени двойки":
    //    только числа вида 1,2,4,8,16,... имеют ровно один установленный бит.
    //
    // 3) !(n & 0xAAAAAAAA)
    //    Маска 0xAAAAAAAA в двоичном виде:
    //    1010 1010 1010 1010 1010 1010 1010 1010
    //    У неё установлены ВСЕ "нечётные" позиции битов (1,3,5,7,...).
    //    Для степеней 4 установленная единица всегда стоит на ЧЁТНОЙ позиции
    //    (0, 2, 4, 6, ...), поэтому пересечение с этой маской должно быть 0.
    //
    // В итоге n — степень 4, если:
    //   - не ноль,
    //   - степень 2,
    //   - установленный бит стоит на чётной позиции.
    return n != 0 && ((n & (n - 1)) == 0) && !(n & 0xAAAAAAAA);
}

int main(void)
{
    uint32_t n;

    // Считываем число
    printf("Enter number: ");
    if (scanf("%u", &n) != 1) {
        printf("Input error\n");
        return 1;
    }

    // Проверяем, является ли оно степенью 4
    isPowerOfFour(n)    ?   printf("YES\n") :   printf("NO\n");

    return 0;
}