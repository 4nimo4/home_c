    int *pi = &num;           // pi —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ num –∫–∞–∫ –Ω–∞ int
    short *ps = (short*)pi;   // –Ø–í–ù–û–ï –ø—Ä–∏–≤–µ–¥–µ–Ω–∏–µ: —Ç–æ—Ç –∂–µ –∞–¥—Ä–µ—Å, –Ω–æ —Ç—Ä–∞–∫—Ç—É–µ–º –∫–∞–∫ short*

