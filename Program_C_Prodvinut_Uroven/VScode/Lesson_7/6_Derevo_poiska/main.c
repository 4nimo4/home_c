void insert(tree **root, int key, tree *pt)
{
    if (!(*root)) {
        // –î–µ—Ä–µ–≤–æ –ø—É—Å—Ç–æ–µ –∏–ª–∏ –¥–æ—à–ª–∏ –¥–æ "–¥—ã—Ä—ã" ‚Äî —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª
        *root = calloc(1, sizeof(tree));  // –ø–∞–º—è—Ç—å –æ–±–Ω—É–ª–µ–Ω–∞ (left/right = NULL)
        (*root)->key    = key;            // –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –∫–ª—é—á
        (*root)->parent = pt;             // –∑–∞–ø–æ–º–∏–Ω–∞–µ–º —Ä–æ–¥–∏—Ç–µ–ª—è

        // –° calloc —Å—Ç—Ä–æ—á–∫–∞ –Ω–∏–∂–µ –Ω–µ –Ω—É–∂–Ω–∞: —É–∫–∞–∑–∞—Ç–µ–ª–∏ —É–∂–µ —Ä–∞–≤–Ω—ã NULL
        // (*root)->left = (*root)->right = NULL;
    }
    else if (key < (*root)->key)
        // –ù–æ–≤—ã–π –∫–ª—é—á –º–µ–Ω—å—à–µ —Ç–µ–∫—É—â–µ–≥–æ ‚Äî –∏–¥—ë–º –≤ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        insert(&((*root)->left), key, *root);
    else
        // –ù–æ–≤—ã–π –∫–ª—é—á >= —Ç–µ–∫—É—â–µ–≥–æ ‚Äî –∏–¥—ë–º –≤ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        // (—Ç–∞–∫ —Ç—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—à—å –¥—É–±–ª–∏–∫–∞—Ç—ã: –æ–Ω–∏ —É—Ö–æ–¥—è—Ç –≤–ø—Ä–∞–≤–æ)
        insert(&((*root)->right), key, *root);
}

