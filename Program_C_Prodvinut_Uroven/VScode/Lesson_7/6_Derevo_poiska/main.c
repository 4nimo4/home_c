//  *root ‚Äî –∞–¥—Ä–µ—Å —É–∫–∞–∑–∞—Ç–µ–ª—è –Ω–∞ —Ç–µ–∫—É—â–∏–π —É–∑–µ–ª (–∏–ª–∏ –∫–æ—Ä–µ–Ω—å),
