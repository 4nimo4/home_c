// –£–∑–µ–ª –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞ –ø–æ–∏—Å–∫–∞
typedef struct tree {
    int key;                 // –ö–ª—é—á (–∑–Ω–∞—á–µ–Ω–∏–µ) —É–∑–ª–∞
    struct tree *left;       // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
    struct tree *right;      // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
    struct tree *parent;     // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π —É–∑–µ–ª
} tree;

//------------------------------------------------------
// –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –≤—ã—Å–æ—Ç—ã –¥–µ—Ä–µ–≤–∞
//------------------------------------------------------
int heightTree(tree* p)
{
    if (p == NULL)
        return 0;  // –í—ã—Å–æ—Ç–∞ –ø—É—Å—Ç–æ–≥–æ –¥–µ—Ä–µ–≤–∞ = 0
    else 
    {
        // –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –≤—ã—á–∏—Å–ª—è–µ–º –≤—ã—Å–æ—Ç—É –ª–µ–≤–æ–≥–æ –∏ –ø—Ä–∞–≤–æ–≥–æ –ø–æ–¥–¥–µ—Ä–µ–≤—å–µ–≤
        int lheight = heightTree(p->left);
        int rheight = heightTree(p->right);

        // –í—ã—Å–æ—Ç–∞ —Ç–µ–∫—É—â–µ–≥–æ –¥–µ—Ä–µ–≤–∞ = –º–∞–∫—Å–∏–º—É–º(–≤—ã—Å–æ—Ç–∞ –ª–µ–≤–æ–≥–æ, –≤—ã—Å–æ—Ç–∞ –ø—Ä–∞–≤–æ–≥–æ) + 1 (–∑–∞ —Ç–µ–∫—É—â–∏–π —É–∑–µ–ª)
        if (lheight > rheight)
            return (lheight + 1);
        else
            return (rheight + 1);
    }
}

//------------------------------------------------------
// –ü–µ—á–∞—Ç—å –≤—Å–µ—Ö —É–∑–ª–æ–≤ –Ω–∞ –∑–∞–¥–∞–Ω–Ω–æ–º —É—Ä–æ–≤–Ω–µ (–¥–ª—è –æ–±—Ö–æ–¥–∞ –≤ —à–∏—Ä–∏–Ω—É)
// level = 1 –æ–∑–Ω–∞—á–∞–µ—Ç ¬´–ø–µ—á–∞—Ç–∞–µ–º —ç—Ç–æ—Ç —É–∑–µ–ª¬ª
//------------------------------------------------------
void printCurrentLevel(tree* root, int level)
{
    if (root == NULL)
        return;         // –ù–µ—á–µ–≥–æ –ø–µ—á–∞—Ç–∞—Ç—å (–ø—É—Å—Ç–æ–π —É–∫–∞–∑–∞—Ç–µ–ª—å)

    else if (level == 1)  // –ú—ã ¬´—Å–ø—É—Å—Ç–∏–ª–∏—Å—å¬ª —Ä–æ–≤–Ω–æ –Ω–∞ –Ω—É–∂–Ω—ã–π —É—Ä–æ–≤–µ–Ω—å
        printf("%d ", root->key);   // –ü–µ—á–∞—Ç–∞–µ–º –∫–ª—é—á —Ç–µ–∫—É—â–µ–≥–æ —É–∑–ª–∞

    else if (level > 1) 
    {
        // –£–º–µ–Ω—å—à–∞–µ–º level –∏ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ —Å–ø—É—Å–∫–∞–µ–º—Å—è:
        // —Å–Ω–∞—á–∞–ª–∞ –≤ –ª–µ–≤–æ–µ, –ø–æ—Ç–æ–º –≤ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        printCurrentLevel(root->left, level - 1);
        printCurrentLevel(root->right, level - 1);
    }
}

//------------------------------------------------------
// –û–±—Ö–æ–¥ –¥–µ—Ä–µ–≤–∞ –≤ —à–∏—Ä–∏–Ω—É (BFS) —á–µ—Ä–µ–∑ —É—Ä–æ–≤–Ω–∏
// –†–µ–∞–ª–∏–∑–æ–≤–∞–Ω –Ω–µ —á–µ—Ä–µ–∑ –æ—á–µ—Ä–µ–¥—å, –∞ —á–µ—Ä–µ–∑:
//   1) –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ –≤—ã—Å–æ—Ç—ã,
//   2) –ø–µ—á–∞—Ç—å —É—Ä–æ–≤–Ω–µ–π –æ–¥–∏–Ω –∑–∞ –¥—Ä—É–≥–∏–º.
//------------------------------------------------------
void printBFS(tree* root)
{
    int h = heightTree(root);        // –≤—ã—Å–æ—Ç–∞ –¥–µ—Ä–µ–≤–∞
    for (int i = 1; i <= h; i++)     // –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —É—Ä–æ–≤–Ω—è –æ—Ç 1 –¥–æ h
        printCurrentLevel(root, i);  // –ø–µ—á–∞—Ç–∞–µ–º –≤—Å–µ —É–∑–ª—ã —ç—Ç–æ–≥–æ —É—Ä–æ–≤–Ω—è
}

//------------------------------------------------------
// –°–∏–º–º–µ—Ç—Ä–∏—á–Ω—ã–π –æ–±—Ö–æ–¥ (Inorder): Left, Root, Right
// –î–ª—è –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞ –ø–æ–∏—Å–∫–∞ –¥–∞—ë—Ç –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –≤—ã–≤–æ–¥ –∫–ª—é—á–µ–π.
//------------------------------------------------------
void inorder(tree *root)
{
    if (root == NULL)
        return;                      // –ø—É—Å—Ç–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ

    if (root->left)
        inorder(root->left);         // —Å–Ω–∞—á–∞–ª–∞ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ

    printf("%d ", root->key);        // –∑–∞—Ç–µ–º —Å–∞–º —É–∑–µ–ª

    if (root->right)
        inorder(root->right);        // –∑–∞—Ç–µ–º –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
} 

//------------------------------------------------------
// –í—Å—Ç–∞–≤–∫–∞ –∫–ª—é—á–∞ –≤ –±–∏–Ω–∞—Ä–Ω–æ–µ –¥–µ—Ä–µ–≤–æ –ø–æ–∏—Å–∫–∞
//   root ‚Äî –∞–¥—Ä–µ—Å —É–∫–∞–∑–∞—Ç–µ–ª—è –Ω–∞ –∫–æ—Ä–µ–Ω—å (–∏–ª–∏ –Ω–∞ —Ç–µ–∫—É—â–∏–π –ø–æ–¥—É–∑–µ–ª),
//   key  ‚Äî –¥–æ–±–∞–≤–ª—è–µ–º—ã–π –∫–ª—é—á,
//   pt   ‚Äî —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ä–æ–¥–∏—Ç–µ–ª—è (–¥–ª—è –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è –ø–æ–ª—è parent).
//
// –ï—Å–ª–∏ *root == NULL, —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª –≤ —ç—Ç–æ–º –º–µ—Å—Ç–µ.
// –ò–Ω–∞—á–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ —Å–ø—É—Å–∫–∞–µ–º—Å—è –≤–ª–µ–≤–æ –∏–ª–∏ –≤–ø—Ä–∞–≤–æ.
//------------------------------------------------------
void insert(tree **root, int key, tree *pt)
{
    if (!(*root)) {
        // –î–æ—à–ª–∏ –¥–æ –ø—É—Å—Ç–æ–≥–æ –º–µ—Å—Ç–∞ ‚Äî —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª
        *root = calloc(1, sizeof(tree));  // –ø–∞–º—è—Ç—å –æ–±–Ω—É–ª–µ–Ω–∞ (left/right = NULL)
        (*root)->key = key;               // –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –∫–ª—é—á
        (*root)->parent = pt;             // —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ä–æ–¥–∏—Ç–µ–ª—è
        // left –∏ right —É–∂–µ —Ä–∞–≤–Ω—ã NULL
    }
    else if (key < (*root)->key)
        // –ö–ª—é—á –º–µ–Ω—å—à–µ —Ç–µ–∫—É—â–µ–≥–æ ‚Äî –∏–¥—ë–º –≤ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        insert(&((*root)->left), key, *root);
    else
        // –ö–ª—é—á –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–µ–Ω ‚Äî –∏–¥—ë–º –≤ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        // (–¥—É–±–ª–∏–∫–∞—Ç—ã –∫–ª—é—á–µ–π –ø–æ–º–µ—â–∞–µ–º –≤–ø—Ä–∞–≤–æ)
        insert(&((*root)->right), key, *root);
}

//------------------------------------------------------
// –†–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π –ø–æ–∏—Å–∫ –∫–ª—é—á–∞ –≤ –¥–µ—Ä–µ–≤–µ –ø–æ–∏—Å–∫–∞
// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —É–∑–µ–ª —Å –∑–∞–¥–∞–Ω–Ω—ã–º –∫–ª—é—á–æ–º –∏–ª–∏ NULL,
// –µ—Å–ª–∏ —Ç–∞–∫–æ–≥–æ –∫–ª—é—á–∞ –Ω–µ—Ç.
//------------------------------------------------------
tree* search_tree(tree *root, int32_t key)
{
    // –ë–∞–∑–æ–≤—ã–π —Å–ª—É—á–∞–π: –¥–æ—à–ª–∏ –¥–æ NULL –∏–ª–∏ –Ω–∞—à–ª–∏ –Ω—É–∂–Ω—ã–π –∫–ª—é—á
    if (root == NULL || root->key == key)
        return root;

    // –ï—Å–ª–∏ –∏—Å–∫–æ–º—ã–π –∫–ª—é—á –º–µ–Ω—å—à–µ —Ç–µ–∫—É—â–µ–≥–æ ‚Äî –∏—â–µ–º –≤ –ª–µ–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ
    else if (root->key > key)
        return search_tree(root->left, key);

    // –ò–Ω–∞—á–µ ‚Äî –∏—â–µ–º –≤ –ø—Ä–∞–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ
    else
        return search_tree(root->right, key);
}

//------------------------------------------------------
// –ò—Ç–µ—Ä–∞—Ç–∏–≤–Ω—ã–π –ø–æ–∏—Å–∫ –∫–ª—é—á–∞ –≤ –¥–µ—Ä–µ–≤–µ –ø–æ–∏—Å–∫–∞
// –ê–Ω–∞–ª–æ–≥ search_tree, –Ω–æ –±–µ–∑ —Ä–µ–∫—É—Ä—Å–∏–∏.
//------------------------------------------------------
tree* search_tree_i(tree *root, int key)
{  
    tree *find = root;   // –Ω–∞—á–∏–Ω–∞–µ–º –ø–æ–∏—Å–∫ —Å –∫–æ—Ä–Ω—è

    // –ü–æ–∫–∞ –Ω–µ –¥–æ—à–ª–∏ –¥–æ NULL –∏ –Ω–µ –Ω–∞—à–ª–∏ –∫–ª—é—á
    while (find && find->key != key) 
    {
        if (key < find->key)
            find = find->left;   // –∏–¥—ë–º –≤–ª–µ–≤–æ
        else
            find = find->right;  // –∏–¥—ë–º –≤–ø—Ä–∞–≤–æ
    }
    // –ª–∏–±–æ –Ω–∞—à–ª–∏ —É–∑–µ–ª (find->key == key), –ª–∏–±–æ find == NULL
    return find;
}

//------------------------------------------------------
// –î–æ–ø. —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π –ø–æ–∏—Å–∫ (–ø–æ —Å—É—Ç–∏ —Ç–æ –∂–µ —Å–∞–º–æ–µ, —á—Ç–æ search_tree)
//------------------------------------------------------
tree * searchKey(tree *root, int key) {
    if (!root)
        return NULL;                // –ø—É—Å—Ç–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
    if (root->key == key)
        return root;                // –Ω–∞—à–ª–∏ –Ω—É–∂–Ω—ã–π –∫–ª—é—á
    if (key < root->key )
        return searchKey(root->left, key);   // –∏—â–µ–º —Å–ª–µ–≤–∞
    return searchKey(root->right, key);      // –∏–Ω–∞—á–µ –∏—â–µ–º —Å–ø—Ä–∞–≤–∞
}

//------------------------------------------------------
// –ü–æ–∏—Å–∫ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–≥–æ –ø–æ –∫–ª—é—á—É —É–∑–ª–∞ –≤ –ø–æ–¥–¥–µ—Ä–µ–≤–µ
// (—Å–∞–º—ã–π –ª–µ–≤—ã–π —É–∑–µ–ª)
//------------------------------------------------------
tree* min_tree(tree *root) {
    tree *find = root;
    while (find && find->left)      // –∏–¥—ë–º –¥–æ —Å–∞–º–æ–≥–æ –ª–µ–≤–æ–≥–æ —É–∑–ª–∞
    {
        find = find->left;
    }
    return find;                    // –ª–∏–±–æ –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–π —É–∑–µ–ª, –ª–∏–±–æ NULL
}

//------------------------------------------------------
// –ü–æ–∏—Å–∫ —É–∑–ª–∞-¬´–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è¬ª (inorder successor):
// —É–∑–µ–ª —Å –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–º –∫–ª—é—á–æ–º, –∫–æ—Ç–æ—Ä—ã–π –±–æ–ª—å—à–µ –∫–ª—é—á–∞ root.
//------------------------------------------------------
/*
–ï—Å–ª–∏ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ —Ç–µ–∫—É—â–µ–≥–æ —É–∑–ª–∞ –Ω–µ –ø—É—Å—Ç–æ, —Ç–æ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª–µ–º
–±—É–¥–µ—Ç —Å–∞–º—ã–π –ª–µ–≤—ã–π —É–∑–µ–ª –µ–≥–æ –ø—Ä–∞–≤–æ–≥–æ –ø–æ–¥–¥–µ—Ä–µ–≤–∞ (–º–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –∫–ª—é—á —Å–ø—Ä–∞–≤–∞).
–ï—Å–ª–∏ –ø—Ä–∞–≤–æ–≥–æ –ø–æ–¥–¥–µ—Ä–µ–≤–∞ –Ω–µ—Ç, –ø–æ–¥–Ω–∏–º–∞–µ–º—Å—è –≤–≤–µ—Ä—Ö –ø–æ parent, –ø–æ–∫–∞
–Ω–µ –Ω–∞–π–¥—ë–º —É–∑–µ–ª, –¥–ª—è –∫–æ—Ç–æ—Ä–æ–≥–æ —Ç–µ–∫—É—â–∏–π —É–∑–µ–ª –ª–µ–∂–∏—Ç –≤ –ª–µ–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ.
*/
tree* left_follower_key(tree *root) 
{
    if (root == NULL)
        return root;        // –ø—É—Å—Ç–æ–π —É–∫–∞–∑–∞—Ç–µ–ª—å ‚Äî –Ω–µ—Ç –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è

    if (root->right)
        // –ï—Å–ª–∏ –µ—Å—Ç—å –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ, –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å ‚Äî –º–∏–Ω–∏–º—É–º —Å–ø—Ä–∞–≤–∞
        return min_tree(root->right);
    else 
    {
        // –ò–¥—ë–º –≤–≤–µ—Ä—Ö –ø–æ parent
        tree *y = root->parent; // –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª—å–Ω—ã–π –ø—Ä–µ–¥–æ–∫
        tree *x = root;         // —Ç–µ–∫—É—â–∏–π —É–∑–µ–ª
        // –ü–æ–¥–Ω–∏–º–∞–µ–º—Å—è, –ø–æ–∫–∞ –∏–¥—ë–º –ø–æ –ø—Ä–∞–≤—ã–º –≤–µ—Ç–∫–∞–º
        while (y && x == y->right) 
        {
            x = y;
            y = y->parent;
        }
        // –õ–∏–±–æ –Ω–∞—à–ª–∏ –ø—Ä–µ–¥–∫–∞, –≤ —á—å—ë–º –ª–µ–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ –ª–µ–∂–∏—Ç –∏—Å—Ö–æ–¥–Ω—ã–π —É–∑–µ–ª,
        // –ª–∏–±–æ –¥–æ—à–ª–∏ –¥–æ –≤–µ—Ä—à–∏–Ω—ã (y == NULL) –∏ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è –Ω–µ—Ç.
        return y;
    }
}

//------------------------------------------------------
// –£–¥–∞–ª–µ–Ω–∏–µ —É–∑–ª–∞ –∏–∑ –¥–≤–æ–∏—á–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞ –ø–æ–∏—Å–∫–∞
// root ‚Äî –∫–æ—Ä–µ–Ω—å –¥–µ—Ä–µ–≤–∞ (–ø–æ –∑–Ω–∞—á–µ–Ω–∏—é),
// pt   ‚Äî —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —É–¥–∞–ª—è–µ–º—ã–π —É–∑–µ–ª.
// –§—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏ —É–¥–∞–ª—ë–Ω–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç.
//------------------------------------------------------
tree* delete(tree **root, tree* pt) 
{
    tree *remove = NULL, *remove_son;

    // –ï—Å–ª–∏ —É pt –Ω–µ—Ç –¥–µ—Ç–µ–π –∏–ª–∏ —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω —Ä–µ–±—ë–Ω–æ–∫ ‚Äî —É–¥–∞–ª—è–µ–º —Å–∞–º pt
    if (pt->left == NULL || pt->right == NULL)
        remove = pt;
    else
        // –ï—Å–ª–∏ —É pt –¥–≤–∞ –ø–æ—Ç–æ–º–∫–∞ ‚Äî —É–¥–∞–ª—è–µ–º –µ–≥–æ ¬´–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è¬ª
        // (—É–∑–µ–ª —Å–æ —Å–ª–µ–¥—É—é—â–∏–º –ø–æ –≤–µ–ª–∏—á–∏–Ω–µ –∫–ª—é—á–æ–º).
        // –£ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ª–µ–≤–æ–≥–æ —Å—ã–Ω–∞, —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω (–ø—Ä–∞–≤—ã–π) –ø–æ—Ç–æ–º–æ–∫.
        remove = left_follower_key(pt); // –í—Å–µ–≥–¥–∞ —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω (–∏–ª–∏ –Ω–∏ –æ–¥–Ω–æ–≥–æ) –ø–æ—Ç–æ–º–æ–∫

    // –ü—Ä–∏–∫—Ä–µ–ø–ª—è–µ–º –ø–æ—Ç–æ–º–∫–∞ —É–¥–∞–ª—è–µ–º–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –∫ —Ä–æ–¥–∏—Ç–µ–ª—é,
    // –ª–∏–±–æ –¥–µ–ª–∞–µ–º —ç—Ç–æ–≥–æ –ø–æ—Ç–æ–º–∫–∞ –∫–æ—Ä–Ω–µ–º –¥–µ—Ä–µ–≤–∞
    if (remove->left != NULL)       // –µ—Å–ª–∏ –µ—Å—Ç—å –ª–µ–≤—ã–π —Ä–µ–±—ë–Ω–æ–∫ (–¥–ª—è —Å–ª—É—á–∞—è, –∫–æ–≥–¥–∞ remove ‚Äî –Ω–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å)
        remove_son = remove->left;
    else
        remove_son = remove->right; // –∏–Ω–∞—á–µ –±–µ—Ä—ë–º –ø—Ä–∞–≤–æ–≥–æ (–∏–ª–∏ NULL, –µ—Å–ª–∏ –¥–µ—Ç–µ–π –Ω–µ—Ç)

    if (remove_son)                 // –µ—Å–ª–∏ –ø–æ—Ç–æ–º–æ–∫ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç ‚Äî –æ–±–Ω–æ–≤–ª—è–µ–º –µ–≥–æ parent
        remove_son->parent = remove->parent;

    if (remove->parent == NULL)     // –µ—Å–ª–∏ —É–¥–∞–ª—è–µ–º –∫–æ—Ä–µ–Ω—å –≤—Å–µ–≥–æ –¥–µ—Ä–µ–≤–∞
        *root = remove_son;          // –Ω–æ–≤—ã–π –∫–æ—Ä–µ–Ω—å ‚Äî –µ–≥–æ –ø–æ—Ç–æ–º–æ–∫
    else if (remove == remove->parent->left)
        remove->parent->left = remove_son;   // —É–¥–∞–ª—è–µ–º—ã–π —É–∑–µ–ª –±—ã–ª –ª–µ–≤—ã–º —Ä–µ–±—ë–Ω–∫–æ–º
    else
        remove->parent->right = remove_son;  // —É–¥–∞–ª—è–µ–º—ã–π —É–∑–µ–ª –±—ã–ª –ø—Ä–∞–≤—ã–º —Ä–µ–±—ë–Ω–∫–æ–º

    // –ï—Å–ª–∏ —É–¥–∞–ª—è–µ–º –Ω–µ —Å–∞–º pt (–∞ –µ–≥–æ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—è), —Ç–æ –ø–µ—Ä–µ–Ω–æ—Å–∏–º –∑–Ω–∞—á–µ–Ω–∏–µ
    // –∫–ª—é—á–∞ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å –≤ –∏—Å—Ö–æ–¥–Ω—ã–π —É–∑–µ–ª pt.
    if (pt != remove)
        pt->key = remove->key;

    return remove;                  // –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏ —É–¥–∞–ª—ë–Ω–Ω—ã–π —É–∑–µ–ª (–µ–≥–æ –º–æ–∂–Ω–æ free)
}

