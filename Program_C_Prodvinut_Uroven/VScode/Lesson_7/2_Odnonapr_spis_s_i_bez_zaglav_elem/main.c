// –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –∑–≤–µ–Ω–∞ –≤ –Ω–∞—á–∞–ª–æ —Å–ø–∏—Å–∫–∞ –±–µ–∑ –∑–∞–≥–ª–∞–≤–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞. –í–∞—Ä–∏–∞–Ω—Ç 1
