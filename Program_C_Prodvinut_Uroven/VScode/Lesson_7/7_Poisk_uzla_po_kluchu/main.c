#include <stdint.h>
#include <stdlib.h>
#include <stdio.h>

// –£–∑–µ–ª –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞ –ø–æ–∏—Å–∫–∞
typedef struct tree {
    int key;                 // –ö–ª—é—á (–∑–Ω–∞—á–µ–Ω–∏–µ) —É–∑–ª–∞
    struct tree *left;       // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
    struct tree *right;      // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
    struct tree *parent;     // –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ä–æ–¥–∏—Ç–µ–ª—è (–Ω–µ –æ–±—è–∑–∞—Ç–µ–ª–µ–Ω, –Ω–æ –º–æ–∂–µ—Ç –ø—Ä–∏–≥–æ–¥–∏—Ç—å—Å—è)
} tree;

//------------------------------------------------------
// –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –≤—ã—Å–æ—Ç—ã –¥–µ—Ä–µ–≤–∞
//------------------------------------------------------
int heightTree(tree* p)
{
    if (p == NULL)
        return 0;  // –í—ã—Å–æ—Ç–∞ –ø—É—Å—Ç–æ–≥–æ –¥–µ—Ä–µ–≤–∞ = 0
    else 
    {
        // –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –≤—ã—á–∏—Å–ª—è–µ–º –≤—ã—Å–æ—Ç—É –ª–µ–≤–æ–≥–æ –∏ –ø—Ä–∞–≤–æ–≥–æ –ø–æ–¥–¥–µ—Ä–µ–≤—å–µ–≤
        int lheight = heightTree(p->left);
        int rheight = heightTree(p->right);

        // –í—ã—Å–æ—Ç–∞ –¥–µ—Ä–µ–≤–∞ = max(–ª–µ–≤–æ–µ, –ø—Ä–∞–≤–æ–µ) + 1 (–∑–∞ —Ç–µ–∫—É—â–∏–π —É–∑–µ–ª)
        if (lheight > rheight)
            return (lheight + 1);
        else
            return (rheight + 1);
    }
}

//------------------------------------------------------
// –ü–µ—á–∞—Ç—å –≤—Å–µ—Ö —É–∑–ª–æ–≤ –Ω–∞ –∑–∞–¥–∞–Ω–Ω–æ–º —É—Ä–æ–≤–Ω–µ (–¥–ª—è –æ–±—Ö–æ–¥–∞ –≤ —à–∏—Ä–∏–Ω—É)
// level = 1 –æ–∑–Ω–∞—á–∞–µ—Ç ¬´–ø–µ—á–∞—Ç–∞–µ–º —ç—Ç–æ—Ç —É–∑–µ–ª¬ª
//------------------------------------------------------
void printCurrentLevel(tree* root, int level)
{
    if (root == NULL)
        return;         // –ù–∏—á–µ–≥–æ –ø–µ—á–∞—Ç–∞—Ç—å

    else if (level == 1)  // –ú—ã ¬´—Å–ø—É—Å—Ç–∏–ª–∏—Å—å¬ª –¥–æ –Ω—É–∂–Ω–æ–≥–æ —É—Ä–æ–≤–Ω—è
        printf("%d ", root->key);

    else if (level > 1) 
    {
        // –°–Ω–∞—á–∞–ª–∞ —É—Ö–æ–¥–∏–º –≤ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ, —É–º–µ–Ω—å—à–∞—è level
        // –ø–æ—Ç–æ–º ‚Äî –≤ –ø—Ä–∞–≤–æ–µ. –ï—Å–ª–∏ –ø–æ–º–µ–Ω—è—Ç—å –º–µ—Å—Ç–∞–º–∏, –∏–∑–º–µ–Ω–∏—Ç—Å—è
        // –ø–æ—Ä—è–¥–æ–∫ –≤—ã–≤–æ–¥–∞ –Ω–∞ —É—Ä–æ–≤–Ω–µ (—Å–Ω–∞—á–∞–ª–∞ –ø—Ä–∞–≤—ã–µ, –ø–æ—Ç–æ–º –ª–µ–≤—ã–µ).
        printCurrentLevel(root->left, level - 1);
        printCurrentLevel(root->right, level - 1);
    }
}

//------------------------------------------------------
// –û–±—Ö–æ–¥ –¥–µ—Ä–µ–≤–∞ –≤ —à–∏—Ä–∏–Ω—É (BFS) —á–µ—Ä–µ–∑ —É—Ä–æ–≤–Ω–∏
// –†–µ–∞–ª–∏–∑–æ–≤–∞–Ω –Ω–µ —á–µ—Ä–µ–∑ –æ—á–µ—Ä–µ–¥—å, –∞ —á–µ—Ä–µ–∑:
//   1) –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ –≤—ã—Å–æ—Ç—ã,
//   2) –ø–µ—á–∞—Ç—å —É—Ä–æ–≤–Ω–µ–π –æ–¥–∏–Ω –∑–∞ –¥—Ä—É–≥–∏–º.
//------------------------------------------------------
void printBFS(tree* root)
{
    int h = heightTree(root);        // –≤—ã—Å–æ—Ç–∞ –¥–µ—Ä–µ–≤–∞
    for (int i = 1; i <= h; i++)     // –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —É—Ä–æ–≤–Ω—è
        printCurrentLevel(root, i);  // –ø–µ—á–∞—Ç–∞–µ–º –≤—Å–µ —É–∑–ª—ã —ç—Ç–æ–≥–æ —É—Ä–æ–≤–Ω—è
}

//------------------------------------------------------
// –°–∏–º–º–µ—Ç—Ä–∏—á–Ω—ã–π –æ–±—Ö–æ–¥ (Inorder): Left, Root, Right
// –î–ª—è –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞ –ø–æ–∏—Å–∫–∞ –¥–∞—ë—Ç –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –≤—ã–≤–æ–¥ –∫–ª—é—á–µ–π.
//------------------------------------------------------
void inorder(tree *root)
{
    if (root == NULL)
        return;

    if (root->left)
        inorder(root->left);      // —Å–Ω–∞—á–∞–ª–∞ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ

    printf("%d ", root->key);     // –ø–æ—Ç–æ–º —Å–∞–º —É–∑–µ–ª (–∫–æ—Ä–µ–Ω—å –ø–æ–¥–¥–µ—Ä–µ–≤–∞)

    if (root->right)
        inorder(root->right);     // –∑–∞—Ç–µ–º –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
} 

//------------------------------------------------------
// –í—Å—Ç–∞–≤–∫–∞ –∫–ª—é—á–∞ –≤ –±–∏–Ω–∞—Ä–Ω–æ–µ –¥–µ—Ä–µ–≤–æ –ø–æ–∏—Å–∫–∞
//   root ‚Äî –∞–¥—Ä–µ—Å —É–∫–∞–∑–∞—Ç–µ–ª—è –Ω–∞ –∫–æ—Ä–µ–Ω—å (–∏–ª–∏ –Ω–∞ —Ç–µ–∫—É—â–∏–π –ø–æ–¥—É–∑–µ–ª),
//   key  ‚Äî –¥–æ–±–∞–≤–ª—è–µ–º—ã–π –∫–ª—é—á,
//   pt   ‚Äî —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ä–æ–¥–∏—Ç–µ–ª—è (–¥–ª—è –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è –ø–æ–ª—è parent).
//
// –ï—Å–ª–∏ *root == NULL, —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª –∑–¥–µ—Å—å.
// –ò–Ω–∞—á–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ —Å–ø—É—Å–∫–∞–µ–º—Å—è –≤–ª–µ–≤–æ –∏–ª–∏ –≤–ø—Ä–∞–≤–æ.
//------------------------------------------------------
void insert(tree **root, int key, tree *pt)
{
    if (!(*root)) {
        // –î–æ—à–ª–∏ –¥–æ –ø—É—Å—Ç–æ–≥–æ –º–µ—Å—Ç–∞ ‚Äî —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —É–∑–µ–ª
        *root = calloc(1, sizeof(tree));  // –ø–∞–º—è—Ç—å –æ–±–Ω—É–ª–µ–Ω–∞ (left/right = NULL)
        (*root)->key = key;
        (*root)->parent = pt;
        // left –∏ right —É–∂–µ —Ä–∞–≤–Ω—ã NULL –±–ª–∞–≥–æ–¥–∞—Ä—è calloc
    }
    else if (key < (*root)->key)
        // –ö–ª—é—á –º–µ–Ω—å—à–µ ‚Äî –∏–¥—ë–º –≤ –ª–µ–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        insert(&((*root)->left), key, *root);
    else
        // –ö–ª—é—á –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–µ–Ω ‚Äî –∏–¥—ë–º –≤ –ø—Ä–∞–≤–æ–µ –ø–æ–¥–¥–µ—Ä–µ–≤–æ
        // (—Ç.–µ. –¥—É–±–ª–∏–∫–∞—Ç—ã —Ç–æ–∂–µ —É—Ö–æ–¥—è—Ç –≤–ø—Ä–∞–≤–æ)
        insert(&((*root)->right), key, *root);
}

//------------------------------------------------------
// –†–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π –ø–æ–∏—Å–∫ –∫–ª—é—á–∞ –≤ –¥–µ—Ä–µ–≤–µ –ø–æ–∏—Å–∫–∞
// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —É–∑–µ–ª —Å –∑–∞–¥–∞–Ω–Ω—ã–º –∫–ª—é—á–æ–º –∏–ª–∏ NULL,
// –µ—Å–ª–∏ —Ç–∞–∫–æ–≥–æ –∫–ª—é—á–∞ –Ω–µ—Ç.
//------------------------------------------------------
tree* search_tree(tree *root, int32_t key)
{
    // –ë–∞–∑–æ–≤—ã–π —Å–ª—É—á–∞–π: –¥–æ—à–ª–∏ –¥–æ NULL –∏–ª–∏ –Ω–∞—à–ª–∏ –Ω—É–∂–Ω—ã–π –∫–ª—é—á
    if (root == NULL || root->key == key)
        return root;

    // –ï—Å–ª–∏ –∏—Å–∫–æ–º—ã–π –∫–ª—é—á –º–µ–Ω—å—à–µ ‚Äî –∏—â–µ–º –≤ –ª–µ–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ
    else if (root->key > key)
        return search_tree(root->left, key);

    // –ò–Ω–∞—á–µ ‚Äî –≤ –ø—Ä–∞–≤–æ–º –ø–æ–¥–¥–µ—Ä–µ–≤–µ
    else
        return search_tree(root->right, key);
}

//------------------------------------------------------
// –ò—Ç–µ—Ä–∞—Ç–∏–≤–Ω—ã–π –ø–æ–∏—Å–∫ –∫–ª—é—á–∞ –≤ –¥–µ—Ä–µ–≤–µ –ø–æ–∏—Å–∫–∞
// –ê–Ω–∞–ª–æ–≥ search_tree, –Ω–æ –±–µ–∑ —Ä–µ–∫—É—Ä—Å–∏–∏.
//------------------------------------------------------
tree* search_tree_i(tree *root, int key)
{  
    tree *find = root;

    // –ü–æ–∫–∞ –Ω–µ –¥–æ—à–ª–∏ –¥–æ NULL –∏ –Ω–µ –Ω–∞—à–ª–∏ –∫–ª—é—á
    while (find && find->key != key) 
    {
        if (key < find->key)
            find = find->left;   // –∏–¥—ë–º –≤–ª–µ–≤–æ
        else
            find = find->right;  // –∏–¥—ë–º –≤–ø—Ä–∞–≤–æ
    }
    // –ª–∏–±–æ –Ω–∞—à–ª–∏ –∫–ª—é—á, –ª–∏–±–æ –≤–µ—Ä–Ω—É–ª–∏ NULL
    return find;
}

//------------------------------------------------------
// main ‚Äî —Å–æ–∑–¥–∞–Ω–∏–µ –¥–µ—Ä–µ–≤–∞, –ø–µ—á–∞—Ç—å, –ø–æ–∏—Å–∫
//------------------------------------------------------
int main(void)
{
    tree *tr = NULL; // –ø—É—Å—Ç–æ–µ –¥–µ—Ä–µ–≤–æ

    // –°—Ç—Ä–æ–∏–º –¥–µ—Ä–µ–≤–æ –ø–æ–∏—Å–∫–∞
    insert(&tr, 10, NULL);
    insert(&tr, 5,  NULL);
    insert(&tr, 15, NULL);
    insert(&tr, 3,  NULL);
    insert(&tr, 7,  NULL);
    insert(&tr, 13, NULL);
    insert(&tr, 18, NULL);
    insert(&tr, 1,  NULL);
    insert(&tr, 6,  NULL);

    printf("\nBFS\n");
    printBFS(tr);
    printf("\n");

    printf("Inorder\n");
    inorder(tr);
    printf("\n");

    // –î–æ–±–∞–≤–∏–º –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç (8)
    insert(&tr, 8, NULL);

    printf("BFS\n");
    printBFS(tr);
    printf("\n");

    printf("Inorder\n");
    inorder(tr);
    printf("\n");

    // –ü–æ–∏—Å–∫ –∫–ª—é—á–∞ 7 (—Ä–µ–∫—É—Ä—Å–∏–≤–Ω–∞—è –≤–µ—Ä—Å–∏—è)
    tree *node = search_tree(tr, 7);
    if (node)
        printf("\nsearch_tree = %d\n", node->key);
    else
        printf("\nNO\n");

    // –ü–æ–∏—Å–∫ –∫–ª—é—á–∞ 7 (–∏—Ç–µ—Ä–∞—Ç–∏–≤–Ω–∞—è –≤–µ—Ä—Å–∏—è)
    node = search_tree_i(tr, 7);
    if (node)
        printf("\nsearch_tree_i = %d\n", node->key);
    else
        printf("\nNO_i\n");

    // –ï—Å–ª–∏ –Ω–∞—à–ª–∏ —É–∑–µ–ª (node –æ—Ç –∏—Ç–µ—Ä–∞—Ç–∏–≤–Ω–æ–≥–æ –ø–æ–∏—Å–∫–∞),
    // –ø–µ—á–∞—Ç–∞–µ–º –ø–æ–¥–¥–µ—Ä–µ–≤–æ —Å —ç—Ç–∏–º —É–∑–ª–æ–º –∫–∞–∫ –∫–æ—Ä–Ω–µ–º (Inorder)
    if (node)
    {
        printf("\nSubtree inorder from found node:\n");
        inorder(node);
        printf("\n");
    }
    else
        printf("\nNO\n");

    return 0;
}