int main()
{
     int n; // Входное значение (секунды)
    printf("Введите количество секунд (0-86399): ");
    scanf("%d", &n);

    // Проверка диапазона
    if (n < 0 || n >= 86400) {
        printf("Ошибка! Число должно быть в диапазоне [0, 86399]\n");
        return 1;
    }

    // Вычисление часов и минут
    int hours = n / 3600;        // Полные часы
    int remaining_seconds = n % 3600; // Остаток после часов
    int minutes = remaining_seconds / 60; // Полные минуты
    int seconds = remaining_seconds % 60;

    printf("Прошло: %d часов, %d минут и %d секунд\n", hours, minutes, seconds);
    return 0;
}
